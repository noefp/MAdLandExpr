<p>
  Combimatrix microarray expression data from several developmental stages of <i>P. patens</i> Gransden WT and Reute WT.
</p>

<h4>Experimental Conditions (3 replicates unless otherwise noted):</h4>


<ul>
  <li><span style="color:blue;">Re - Germinating spores - Ksl</span></li>
  <li><span style="color:red;">Gd - Protonema - Ksl</span></li>
  <li><span style="color:red;">Gd - Protonema - KlqA</span></li>
  <li><span style="color:green;">Gd - Juvenile gametophores - Ksl</span></li>
  <li><span style="color:green;">Gd - Adult gametophores - Ksl</span></li>
  <li><span style="color:green;">Re - Adult gametophores - Ksl</span></li>
  <li><span style="color:orange;">Re - Sporophytes LB-B - Ksl</span></li>
  <li><span style="color:#FFCC00;">Gd - Protoplasts - Klq</span></li>
</ul>

  
<p>
  Gd: Gransden WT<br>
  Re: Reute WT<br>
  Note: Gametophores with sexual organs are considered Adult gametophores.
</p>

<p>
  These data were published in <a href="http://onlinelibrary.wiley.com/doi/10.1111/tpj.12572/full">Hiss et al., 2014</a>. The data are from the CombiMatrix microarray platform and all the samples were normalized by a median value of 12. <br>Gene models: v1.2 Phypa.
</p>

