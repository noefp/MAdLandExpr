<p>
  RNA-seq Expression data from several developmental stages of <i>P. patens</i> Gransden WT and Reute WT.
</p>

<h4>Experimental Conditions (3 replicates unless otherwise noted):</h4>

<ul>
  <li><span style="color:blue;">Gd - Dry Spores</span> [2 replicates]</li>
  <li><span style="color:blue;">Gd - Imbibed Spores BlqA</span> [1 replicate]</li>
  <li><span style="color:blue;">Gd - Germinating spores BslA</span></li>
  <li><span style="color:red;">Gd - Protonema Bsl</span></li>
  <li><span style="color:red;">Gd - Protonema Ksl</span></li>
  <li><span style="color:red;">Gd - Protonema Klq</span></li>
  <li><span style="color:red;">Re - Protonema Klq</span></li>
  <li><span style="color:green;">Gd - Adult Gametophores Bsl</span></li>
  <li><span style="color:green;">Gd - Adult Gametophores Ksl</span></li>
  <li><span style="color:green;">Re - Adult Gametophores Ksl</span></li>
  <li><span style="color:green;">Re - Gametophores Ksl</span></li>
  <li><span style="color:green;">Gd - Leaflets Ksl</span> [2 replicates]</li>
  <li><span style="color:orange;">Re - Green Sporophyte Ksl</span></li>
  <li><span style="color:orange;">Re - Brown Sporophyte Ksl</span></li>
  <li><span style="color:#FFCC00;">Gd - Protoplast BslA</span></li>
  
</ul>

  
<p>
  Gd: Gransden WT<br>
  Re: Reute WT<br>
  Note: Gametophores with sexual organs were considered Adult gametophores.
</p>

<p>
  Dry spores and imbibed spores from Grandsen and protonema Klq, gametophore ksl and adult gametophore ksl from Reute were published in <a href="https://onlinelibrary.wiley.com/doi/10.1111/tpj.14607">Fernandez-Pozo et al., 2020</a>. The rest of the experiments were published in <a href="https://onlinelibrary.wiley.com/doi/abs/10.1111/tpj.13940">Perroud et al., 2018</a>. All samples were normalized to RPKM.
</p>

