<p>
Zygnematophyceae are the algal sisters of land plants.
This class is named for filamentous algae of the genus Zygnema, which are known to occur in challenging environments and are desiccation resistant. 
Four strains (SAG 698-1a, SAG 698-1b, UTEX 1559, UTEX 1560) were sequenced, capturing the strain history of the + and - mating types derived from the Zygnema circumcarinatum sample of 1929 by Czurda. 
Of these, SAG 698-1a likely now represents a different species, Zygnema cf. cylindricum. 
Owing to their key phylogenetic position, genome data on these organisms shed light on the evolution of the genetic toolbox for multicellular growth and response to environmental challenges.
</p>
